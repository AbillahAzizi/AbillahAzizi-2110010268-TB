    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Mendapatkan baris yang dipilih dari tabel
        int selectedRow = TabelProduk.getSelectedRow();

        // Memastikan baris terpilih sebelum melanjutkan
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit");
            return;
        }
        
        // Mendapatkan nilai kolom dari baris terpilih
        String id_produk = TabelProduk.getValueAt(selectedRow, 0).toString();
        String id_kategori = TabelProduk.getValueAt(selectedRow, 1).toString();
        String nama_produk = TabelProduk.getValueAt(selectedRow, 2).toString();
        String harga = TabelProduk.getValueAt(selectedRow, 3).toString();
        String stok = TabelProduk.getValueAt(selectedRow, 4).toString();

        // Menampilkan dialog untuk memasukkan nilai yang baru
        String newIdKat = JOptionPane.showInputDialog(this, "Masukkan ID Kategori Baru:", id_kategori);
        String newNamaProd = JOptionPane.showInputDialog(this, "Masukkan Nama Produk Baru:", nama_produk);
        String newHarga = JOptionPane.showInputDialog(this, "Masukkan Harga Baru:", harga);
        String newStok = JOptionPane.showInputDialog(this, "Masukkan Stok Baru:", stok);
        
        // Memastikan bahwa pengguna memasukkan nilai yang valid dan tidak membatalkan input
        if (newIdKat != null && !newIdKat.isEmpty() && newNamaProd != null && !newNamaProd.isEmpty()
                && newHarga != null && !newHarga.isEmpty() && newStok != null && !newStok.isEmpty()) {

            // Update data ke dalam database MySQL
            try {
                Connection kon = koneksi.koneksiDb();
                Statement st = kon.createStatement();
                String sql_update = "UPDATE tbl_produk SET id_kategori = '" + newIdKat + "', nama_produk = '" + newNamaProd +
                                    "', harga = '" + newHarga + "', stok = '" + newStok + "' WHERE id_produk = '" + id_produk + "'";
                st.executeUpdate(sql_update);

                // Tampilkan pesan sukses
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate");

                // Perbarui tabel untuk menampilkan data yang telah diperbarui
                load_data();
                IDOtomatis();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Mendapatkan kata kunci pencarian dari TextField
        String keyword = jTextField1.getText().trim();

        // Memastikan bahwa kata kunci tidak kosong sebelum melanjutkan
        if (!keyword.isEmpty()) {
            // Melakukan pencarian berdasarkan nama produk di database MySQL
            try {
                Connection kon = koneksi.koneksiDb();
                Statement st = kon.createStatement();
                String sql_search = "SELECT * FROM tbl_produk WHERE nama_produk LIKE '%" + keyword + "%'";
                ResultSet rs = st.executeQuery(sql_search);

                // Mengisi tabel dengan hasil pencarian
                DefaultTableModel data = new DefaultTableModel();
                data.addColumn("ID PRODUK");
                data.addColumn("ID KATEGORI");
                data.addColumn("NAMA PRODUK");
                data.addColumn("HARGA");
                data.addColumn("STOK");

                while (rs.next()) {
                    String d1 = rs.getString(1);
                    String d2 = rs.getString(2);
                    String d3 = rs.getString(3);
                    String d4 = rs.getString(4);
                    String d5 = rs.getString(5);
                    String[] d = {d1, d2, d3, d4, d5};
                    data.addRow(d);
                }

                // Menampilkan hasil pencarian di tabel
                TabelProduk.setModel(data);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        } else {
            // Jika kata kunci kosong, tampilkan pesan peringatan
            JOptionPane.showMessageDialog(this, "Masukkan kata kunci pencarian");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

