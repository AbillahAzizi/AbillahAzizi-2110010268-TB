    public void load_data(){
        Connection kon=koneksi.koneksiDb();
        Object header[]={"ID PRODUK","ID KATEGORI","NAMA PRODUK","HARGA","STOK"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        TabelProduk.setModel(data);
        String sql_data="SELECT * FROM tbl_produk";
        
        try
        {
            Statement st=kon.createStatement();
            ResultSet rs=st.executeQuery(sql_data);
            while(rs.next())
            {
                String d1=rs.getString(1);
                String d2=rs.getString(2);
                String d3=rs.getString(3);
                String d4=rs.getString(4);
                String d5=rs.getString(5);
                
                String d[]={d1,d2,d3,d4,d5};
                data.addRow(d);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    public void IDOtomatis(){
        
        try
        {
            Connection kon=koneksi.koneksiDb();
            Statement st=kon.createStatement();
            String sql_id="SELECT * FROM tbl_produk ORDER BY id_produk";
            ResultSet rs=st.executeQuery(sql_id);
            if(rs.next())
            {
                String id_produk=rs.getString("id_produk").substring(1);
                String AN=""+(Integer.parseInt(id_produk)+1);
                String Nol="";
                if(AN.length()==1)
                {
                    Nol="0000";
                }
                else if(AN.length()==2)
                {
                    Nol="000";
                }
                else if(AN.length()==3)
                {
                    Nol="00";
                }
                IDPROD.setText("A"+Nol+AN);
            }
            else
            {
                IDPROD.setText("A00001");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }

